package eu.ewall.platform.idss.service.model.type;

/**
 * The possible weather types, based on the weather icons that are available
 * at
 * <a href="http://openweathermap.org/weather-conditions">http://openweathermap.org/weather-conditions</a>
 * 
 * @author Dennis Hofs (RRD)
 */
public enum WeatherType {
	CLEAR,
	FEW_CLOUDS,
	SCATTERED_CLOUDS,
	BROKEN_CLOUDS,
	SHOWER_RAIN,
	RAIN,
	THUNDERSTORM,
	SNOW,
	MIST
}
